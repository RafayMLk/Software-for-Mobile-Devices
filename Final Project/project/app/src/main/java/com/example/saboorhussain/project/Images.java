package com.example.saboorhussain.project;

public class Images {
    private String uid;
    private String imageadress;

    public Images() {
    }

    public Images(String uid, String imageadress) {
        this.uid = uid;
        this.imageadress = imageadress;

    }


    public String getUid() {
        return uid;
    }

    public String getImageadress() {
        return imageadress;
    }
}
